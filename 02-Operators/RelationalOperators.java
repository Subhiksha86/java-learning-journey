public class RelationalOperators 
{

    public static void main(String[] args) 
    {
        int a = 5;
        int b = 8 ;
        boolean greater = a > b ;

        System.out.println(greater);

        double cgpa = 9.18;
        int  year =  2;
        boolean eligible = year <= cgpa ;

        System.out.println(eligible);

        float subhi = 9.88f;
        float thoufi  = 9.99f;
        boolean different = subhi == thoufi;

        System.out.println(!different);

        String x = "Hello";
        String y = "Hiiii";

        System.out.println( x != y);
    

        
    }
    
}
